    private void btnInserirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInserirMouseClicked
        // TODO add your handling code here:
       Connection con = ConexaoBD.abrirConexao();
       DadosClientesBean cb = new DadosClientesBean();
       DadosClientesDAO cd = new DadosClientesDAO(con);
       
       cb.setNome(txtNome.getText());
       cb.setTelefone(txtTelefone.getText());
       cb.setEndereco(txtEndereco.getText());
       cb.setBairro(txtBairro.getText());
       cb.setCidade(txtCidade.getText());
       cb.setUf(txtUf.getText());
       cb.setCep(txtCep.getText());
       
       
      lblMensagem.setText(cd.inserir(cb));
      
      ConexaoBD.fecharConexao(con);
      
     if(lblMensagem.getText() == "inserido com sucesso"){
          Equipamento eq = new Equipamento();
          eq.show();
          this.hide();
      }
       
  
    }//GEN-LAST:event_btnInserirMouseClicked

